<h2>Casa da Esperança Cubatão service management system <span class="emoji">✨ ✨</span></h2>

<p>
	System responsible for providing an interface where user can register appointments, manage them
	and keep record of children in social vulnerability assistance. The system has enable the NGO to
	improve their service quality, perfomance and overall satisfaction from the internal employees and
	end users.
</p>

<p>
	<b>Technicalities:</b> The system is build as a React Single Page application, using
	<a target="_blank" href="https://material-ui.com/">material ui</a>
	framework for interface components. It consumes a rest API based on
	<a href="https://strapi.io/" target="_blank">Strapi</a>
	headless CMS, hosted on a cloud based infrastructure on Digital Ocean
</p>
